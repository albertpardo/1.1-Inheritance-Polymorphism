package level3ex1;

public class PointsPricesForNews {

    private double price;
    private double points;

    public PointsPricesForNews(double price, double points){
        this.price = price;
        this.points = points;
    }

    public double getPoints() {
        return points;
    }

    public double getPrice() {
        return price;
    }
}
